weekno=int(input("enter a week no."))
if weekno==1:
    print("MONDAY")
elif weekno==2:
    print("TUESDAY")
elif weekno==3:
    print("WEDNESDAY")
elif weekno==4:
    print("THURSDAY")
elif weekno==5:
    print("FRIDAY")
elif weekno==6:
    print("SATURDAY")
elif weekno==7:
    print("SUNDAY")
else:
    print("invalid weekno.")